    private void TableBookingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableBookingMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)TableBooking.getModel();
        int row = TableBooking.getSelectedRow();
        Booking b = new Booking().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        TextIdbooking.setText(String.valueOf(b.getIdBooking()));
        Textnama.setText(b.getData().getNama());
        Combopayment.getModel().setSelectedItem(b.getTipePayment());
        Textkelas.setText(b.getKelas());
        Textstatus.setText(b.getStatus());
        TextHarga.setText(String.valueOf(b.getTotalharga()));
        Textcredit.setText(String.valueOf(b.getNoCredit()));
        click = true;
    }//GEN-LAST:event_TableBookingMouseClicked


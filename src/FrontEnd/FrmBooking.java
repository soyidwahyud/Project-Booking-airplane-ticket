    public void tampilkanData(){
        String [] kolom = {"id booking","Nama","Nama Maskapai","kode kursi","kelas","status", "total harga","tipe payment", "no credit"};
        ArrayList<Booking> list = new Booking().getAll();
        Object row[] = new Object[9];
        TableBooking.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getIdBooking();
            row[1] = list.get(i).getData().getNama();
            row[2] = list.get(i).getMaskapai().getNama();
            row[3] = list.get(i).getKodeKursi();
            row[4] = list.get(i).getKelas();
            row[5] = list.get(i).getStatus();
            row[6] = list.get(i).getTotalharga();
            row[7] = list.get(i).getTipePayment();
            row[8] = list.get(i).getNoCredit();
            ((DefaultTableModel)TableBooking.getModel()).addRow(row);
        }
    }
  


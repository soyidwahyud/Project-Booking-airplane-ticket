    public void tampilkanData(){
        String [] Kolom = {"Id","Departure","Destination","Tanggal"};
        ArrayList<Jadwal> List = new Jadwal().getAll();
        Object row[] = new Object[4];
        jTable1.setModel(new DefaultTableModel(new Object[][]{}, Kolom));
        for (int i = 0; i<List.size(); i++) {
            row[0] = List.get(i).getIdjadwal();
            row[1] = List.get(i).getDepature();
            row[2] = List.get(i).getDestination();
            row[3] = List.get(i).getDeparturedate();
            ((DefaultTableModel)jTable1.getModel()).addRow(row);
        }
    }


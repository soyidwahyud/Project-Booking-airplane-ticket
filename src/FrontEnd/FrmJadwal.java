    private void jHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHapusActionPerformed
        // TODO add your handling code here:
        if (click) {
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int row = jTable1.getSelectedRow();
            Jadwal j = new Jadwal().getById(Integer.parseInt(model.getValueAt(row,0).toString()));
            j.delete();
            Kosongkan();
            tampilkanData();
        } else {
            
        }
        
    }//GEN-LAST:event_jHapusActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int row = jTable1.getSelectedRow();
        Jadwal j = new Jadwal().getById(Integer.parseInt(model.getValueAt(row,0).toString()));
        jIdjadwal.setText(String.valueOf(j.getIdjadwal()));
        inputDeparture.setText(j.getDepature());
        inputDate.setText(j.getDeparturedate());
        inputDestination.setText(j.getDestination());
        click = true;
    }//GEN-LAST:event_jTable1MouseClicked


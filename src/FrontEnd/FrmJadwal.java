    private void jsimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jsimpan1ActionPerformed
        // TODO add your handling code here:
            Jadwal jadwal = new Jadwal();
            jadwal.setIdjadwal(Integer.parseInt(jIdjadwal.getText()));
            jadwal.setDepature(inputDeparture.getText());
            jadwal.setDestination(inputDestination.getText());
            jadwal.setDeparturedate(inputDate.getText());
            //System.out.println(jadwal.getIdjadwal());
            jadwal.save();
            jIdjadwal.setText(Integer.toString(jadwal.getIdjadwal()));
            tampilkanData();
        
    }//GEN-LAST:event_jsimpan1ActionPerformed


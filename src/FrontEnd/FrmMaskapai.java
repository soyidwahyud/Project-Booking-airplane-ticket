    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        if (click) {
            DefaultTableModel model = (DefaultTableModel)TableMaskapai.getModel();
            int row = TableMaskapai.getSelectedRow();
            Maskapai m = new Maskapai().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
            m.delete();
            kosongkan();
            tampilkanData();
        }else {
            
        }
        
    }//GEN-LAST:event_hapusActionPerformed

    private void TableMaskapaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMaskapaiMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)TableMaskapai.getModel();
        int row = TableMaskapai.getSelectedRow();
        Maskapai m = new Maskapai().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        textIdMaskapai.setText(String.valueOf(m.getIdMaskapai()));
        textNama.setText(m.getNama());
        comboJadwal.getModel().setSelectedItem(m.getJadwal());
        textKedatangan.setText(m.getJadwal().getDestination());
        textKeb.setText(m.getJadwal().getDepature());
        textWaktuDatang.setText(String.valueOf(m.getWaktuDestination()));
        textwaktuBerangkat.setText(String.valueOf(m.getWaktuDeparture()));
        textHargaBisnis.setText(String.valueOf(m.getHarga_bis()));
        textHargaEkonomi.setText(String.valueOf(m.getHarga_eko()));
        click = true;
    }//GEN-LAST:event_TableMaskapaiMouseClicked


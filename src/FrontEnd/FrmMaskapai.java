    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        
        Maskapai m = new Maskapai();
        m.setIdMaskapai(Integer.parseInt(textIdMaskapai.getText()));
        m.setNama(textNama.getText());
        m.setWaktuDeparture(textwaktuBerangkat.getText());
        m.setWaktuDestination(textWaktuDatang.getText());
        m.setJadwal((Jadwal)comboJadwal.getSelectedItem());
        m.setHarga_bis(Double.valueOf(textHargaBisnis.getText()));
        m.setHarga_eko(Double.valueOf(textHargaEkonomi.getText()));
        m.save();
        textIdMaskapai.setText(Integer.toString(m.getIdMaskapai()));
        tampilkanData();
        
    }//GEN-LAST:event_simpanActionPerformed


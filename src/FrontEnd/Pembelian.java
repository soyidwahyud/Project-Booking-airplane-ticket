    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        Data d = new Data();
        d.setIddata(0);
        d.setNama(txtNama.getText());
        d.setNik(txtNIK.getText());
        d.setNotelp(txtTelpon.getText());
        d.setUmur(Integer.valueOf(txtUmur.getText()));
        d.setAlamat(txtAlamat.getText());
        d.save();

        Booking b = new Booking();
        b.setIdBooking(0);
        b.setData(d);
        b.getMaskapai().setIdMaskapai(getIdMas(txtMaskapai.getText(), txtJam.getText()));
        b.setKodeKursi(txtKodeBangku.getText());
        b.setKelas(txtKelas.getText());
        b.setStatus("book");
        b.setTipePayment(tipeBayar);
        b.setNoCredit(txtKartu.getText());
        b.setTotalharga(Double.valueOf(txtTotalHarga.getText()));
        b.save();
        click = true;
        JOptionPane.showMessageDialog(null, "Booking Berhasil");
        //new Tiket().setVisible(true);
        //dispose();
    }//GEN-LAST:event_btnSubmitActionPerformed


    public boolean isBook(String kode, String kelas){
        boolean ans = false;
        for (Booking booking : new Booking().getAll()) {
            if (booking.getKodeKursi().equals(kode) && booking.getKelas().equals(kelas)) {
                if (booking.getStatus().equals("book")) {
                    ans = true;
                }
            }
        }
        return ans;
    }
    public int getIdMas(String maskapai, String jam){
        int id = 0;
        for (Maskapai m : new Maskapai().getAll()) {
            if (m.getNama().equals(maskapai) && m.getWaktuDeparture().equals(jam)) {
                id = m.getIdMaskapai();
            }
        }
        return id;
    }
    public double kelasHarga(String kelas, int idMas){
        Maskapai m = new Maskapai().getById(idMas);
        double harga = 0;
        harga = m.getHarga_eko();
        if (kelas.equals("Bisnis")) {
            harga = m.getHarga_bis();
        }
        return harga;
    }
    public void radioSet(){
        if (buttonGroupPembayaran.isSelected(radioKontan.getModel())) {
            txtCash.setEditable(true);
            txtKartu.setEditable(false);
            txtKartu.setText("");
            tipeBayar = "Cash";
        }
        else {
            txtCash.setEditable(false);
            txtCash.setText("");
            txtKartu.setEditable(true);
            tipeBayar = "Credit";
        }
    }

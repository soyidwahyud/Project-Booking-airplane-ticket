    private void cmbTujuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTujuanActionPerformed
        // TODO add your handling code here:
        Object obj = evt.getSource();
        if (obj == cmbTujuan) {
            cmbMaskapai.setModel(new DefaultComboBoxModel());
            Jadwal des = new Jadwal();
            des = (Jadwal)cmbTujuan.getSelectedItem();
            for (Maskapai maskapai : new Maskapai().getAll()) {
                if (maskapai.getJadwal().getDestination().equals(des.getDestination())) {
                    cmbMaskapai.addItem(maskapai.getNama()+" >"+maskapai.getWaktuDeparture());
                }
            }
        }
        txtKodeBangku.setText("");
    }//GEN-LAST:event_cmbTujuanActionPerformed

